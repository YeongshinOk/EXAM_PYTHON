# 음수면 "OO은 음수입니다"
# 0이면 "OO은 영입니다"를 출력해주세요
# -------------------------------------------------
# 다중 조건문 => 조건문이 2개 이상 되는 경우
if mynum > 0:
    print(f'{mynum}은 양수입니다.')
elif mynum < 0:
    print(f'{mynum}은 음수입니다.')
else:
    print(f'{mynum}은 영입니다.')

