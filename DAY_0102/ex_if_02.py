# [실습] -------------------------------------------
# 좋아하는 정수를 하나 저장한 후 짝수면 "OO은 짝수입니다"
# 홀수면 "OO은 홀수입니다"를 출력해주세요
# -------------------------------------------------
mynum = 71

# 숫자 % 2 == 0 : 짝수, 숫자 % 2 == 1 : 홀수
if mynum % 2 == 0:
    print(f'{mynum}은 짝수입니다.')
else:
    print(f'{mynum}은 홀수입니다.')
if mynum % 2 :
    print(f'{mynum}은 홀수입니다.')
else:
    print(f'{mynum}은 짝수입니다.')

